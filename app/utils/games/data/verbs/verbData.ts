// Helper functions
export const getVerbsByCategory = (
  category: VerbData["category"]
): VerbData[] => {
  return GREEK_VERBS.filter((verb) => verb.category === category);
};

export const getVerbsByDifficulty = (
  difficulty: VerbData["difficulty"]
): VerbData[] => {
  return GREEK_VERBS.filter((verb) => verb.difficulty === difficulty);
};

export const getVerbById = (id: string): VerbData | undefined => {
  return GREEK_VERBS.find((verb) => verb.id === id);
};

export const getRandomVerb = (): VerbData => {
  return GREEK_VERBS[Math.floor(Math.random() * GREEK_VERBS.length)];
};
